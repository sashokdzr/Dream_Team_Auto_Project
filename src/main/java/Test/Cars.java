package Test;

public class Cars {

    private String color = "";
    private double engine;
    private int costs;

    public void setColor(String color) {
        this.color = color;
    }

    public void setEngine(double engine) {
        this.engine = engine;
    }

    public void setCosts(int costs) {
        this.costs = costs;
    }

    public String getColor() {
        return color;
    }

    public double getEngine() {
        return engine;
    }

    public int getCosts() {
        return costs;
    }
}
